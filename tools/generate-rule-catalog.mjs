#!/usr/bin/env node
/**
 * docs/seo-rules/*.json → frontend rule-catalog.generated.ts
 * Tüm crawl + post-crawl kurallarını playbook fallback kataloğuna aktarır.
 */
import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const rulesDir = join(root, "docs", "seo-rules");
const outDir = join(root, "frontend", "apps", "public-app", "src", "app", "features", "audit");
const outFile = join(outDir, "rule-catalog.generated.ts");

const EXTRA_RULES = [
  {
    id: "EEAT-002",
    category: "content-quality",
    severity: "info",
    message: "LLM içerik kalitesi analizi ek bulgu.",
    fixHint: "Google insan odaklı içerik yönergelerine göre yazar, kaynak ve güven sinyallerini güçlendirin.",
    docUrl: "https://developers.google.com/search/docs/fundamentals/creating-helpful-content",
  },
];

const files = (await readdir(rulesDir)).filter((f) => f.endsWith(".json"));
const byId = new Map();

for (const file of files) {
  const raw = await readFile(join(rulesDir, file), "utf8");
  const rules = JSON.parse(raw);
  for (const rule of rules) {
    if (!rule?.id) continue;
    byId.set(rule.id, {
      id: rule.id,
      category: rule.category ?? "",
      severity: rule.severity ?? "",
      message: rule.message ?? "",
      fixHint: rule.fixHint ?? "",
      docUrl: rule.docUrl ?? "",
    });
  }
}

for (const rule of EXTRA_RULES) {
  if (!byId.has(rule.id)) byId.set(rule.id, rule);
}

const sorted = [...byId.values()].sort((a, b) => a.id.localeCompare(b.id));

const lines = sorted.map((r) => {
  const esc = (s) => JSON.stringify(s ?? "");
  return `  ${JSON.stringify(r.id)}: {
    category: ${esc(r.category)},
    severity: ${esc(r.severity)},
    message: ${esc(r.message)},
    fixHint: ${esc(r.fixHint)},
    docUrl: ${esc(r.docUrl)},
  },`;
});

const content = `/* eslint-disable */
// AUTO-GENERATED by tools/generate-rule-catalog.mjs — do not edit manually.
// Run: node tools/generate-rule-catalog.mjs

export interface RuleCatalogEntry {
  category: string;
  severity: string;
  message: string;
  fixHint: string;
  docUrl: string;
}

export const RULE_CATALOG: Record<string, RuleCatalogEntry> = {
${lines.join("\n")}
};

export const RULE_CATALOG_COUNT = ${sorted.length};
`;

await mkdir(outDir, { recursive: true });
await writeFile(outFile, content, "utf8");
console.log(`Generated ${sorted.length} rules → ${outFile}`);
